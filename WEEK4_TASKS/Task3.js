// var example
var name = "Alice";
console.log("var variable:", name);

// let example
let age = 25;
console.log("let variable:", age);

// const example
const country = "USA";
console.log("const variable:", country);

// Demonstrating re-assignment
name = "Bob"; // Re-assignment is allowed with var
console.log("Updated var variable:", name);

age = 30; // Re-assignment is allowed with let
console.log("Updated let variable:", age);

